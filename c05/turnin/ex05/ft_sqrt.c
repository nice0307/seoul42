int	ft_sqrt(int nb)
{
	float	fx;
	int		nx;
	int		n;

	fx = 2.0;
	nx = 2;
	n = 0;
	if (nb < 0)
		return (0);
	while (n < 50)
	{
		nx = (nx + (nb / nx)) / 2;
		fx = (fx + (nb / fx)) / 2.0;
		n++;
	}
	if (fx > nx)
		return (0);
	return (nx);
}
